package javaprograms;

public class Encapsulation111 {
	
	private int i;
	private int j;
	
public void add() {
		
		System.out.println(i+j);
	}
	
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public int getJ() {
		return j;
	}
	public void setJ(int j) {
		this.j = j;
	}

}
